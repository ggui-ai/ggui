/**
 * Run LLM evaluation with parallel per-criterion calls.
 *
 * Runs 7 static criteria (2 tier 1 + 5 tier 2) plus 0-3 dynamic criteria
 * generated from the user's request. All run in parallel with a shared
 * mother prompt (cached by the provider). Wall time ~2s for all criteria.
 *
 * @param preWarmedContext - Optional pre-warmed context from `preWarmEval()`.
 *   If provided, skips mother prompt building and dynamic criteria generation.
 */
export async function runLLMEvaluation(
  context: LLMEvalContext,
  config: LLMEvalConfig,
  preWarmedContext?: PreWarmedEvalContext | null,
): Promise<EvalResult & { inputTokens: number; outputTokens: number }> {
  const providerName = config.provider;
  const model = config.model || getDefaultEvalModel(providerName);
  const routerProvider =
    providerName === 'claude'
      ? ('anthropic' as const)
      : (providerName as 'openai' | 'google');

  // Use pre-warmed context if available, otherwise build fresh
  const motherPrompt = preWarmedContext?.motherPrompt ?? buildMotherPrompt({
    originalPrompt: context.originalPrompt,
    contract: context.contract,
    designSystemSummary: context.designContext,
  });

  // Add line numbers to source
  const numberedSource = context.sourceCode
    .split('\n')
    .map((line, i) => `${String(i + 1).padStart(4)} | ${line}`)
    .join('\n');

  // Create a single agent — all calls share the same client + cache
  const agent = createAgent(routerProvider);

  // Dynamic criteria removed — they generated overly strict edge-case checks
  // that wasted coding turns on nitpicks. Will be replaced by a holistic
  // reviewer in auto-improve mode.

  const evalStart = Date.now();

  const totalCriteriaCount = TIER_1_CRITERIA.length + TIER_2_CRITERIA.length;

  // Run ALL criteria in parallel: 7 static (2 tier 1 + 5 tier 2)
  const results = await Promise.all([
    ...TIER_1_CRITERIA.map(c => evalCriterion(c, 1, motherPrompt, numberedSource, agent, model)),
    ...TIER_2_CRITERIA.map(c => evalCriterion(c, 2, motherPrompt, numberedSource, agent, model)),
  ]);

  // Merge results
  const allIssues: EvalIssue[] = [];
  const passCategories: string[] = [];
  let totalIn = 0;
  let totalOut = 0;
  let cachedTokensEstimate = 0;

  for (const r of results) {
    allIssues.push(...r.issues);
    if (r.pass) {
      passCategories.push(r.criterion);
    }
    totalIn += r.tokens.in;
    totalOut += r.tokens.out;
  }

  // Estimate cached tokens (mother prompt sent N times, cached N-1 times)
  const firstResult = results[0];
  if (firstResult && firstResult.tokens.in > 0) {
    cachedTokensEstimate = firstResult.tokens.in * (totalCriteriaCount - 1);
  }

  const wallMs = Date.now() - evalStart;

  // Log per-criterion results
  for (const r of results) {
    const issueCount = r.issues.length;
    const status = r.pass ? 'PASS' : r.tier === 1 ? 'FAIL' : (r.issues.some(i => i.result === 'fail') ? 'FAIL' : 'WARN');
    const issueLabel = issueCount > 0 ? ` (${issueCount} issue${issueCount > 1 ? 's' : ''})` : '';
    console.log(`[eval] ${r.criterion}: ${status}${issueLabel} ${(r.durationMs / 1000).toFixed(1)}s`);
  }
  console.log(
    `[eval] total: ${(wallMs / 1000).toFixed(1)}s wall (${totalCriteriaCount} parallel) | ` +
    `in=${totalIn} out=${totalOut} cached~=${cachedTokensEstimate} | ` +
    `issues=${allIssues.length} pass=${passCategories.length}`,
  );

  return {
    issues: allIssues,
    pass: passCategories,
    inputTokens: totalIn,
    outputTokens: totalOut,
  };
}
