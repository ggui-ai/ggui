/** Tier 1 tool schema: binary pass/fail with issues list */
function makeTier1Tool(criterion: string): LLMToolDef {
  return {
    name: `evaluate_${criterion}`,
    description: `Submit evaluation result for ${criterion}`,
    parameters: {
      type: 'object',
      required: ['pass'],
      properties: {
        pass: { type: 'boolean' },
        issues: {
          type: 'array',
          items: { type: 'string' },
          description: 'List of specific issues found',
        },
      },
    },
  };
}

/** Tier 2 tool schema: fail/warn/pass with issues list */
function makeTier2Tool(criterion: string): LLMToolDef {
  return {
    name: `evaluate_${criterion}`,
    description: `Submit evaluation result for ${criterion}`,
    parameters: {
      type: 'object',
      required: ['result'],
      properties: {
        result: {
          type: 'string',
          enum: ['fail', 'warn', 'pass'],
        },
        issues: {
          type: 'array',
          items: { type: 'string' },
          description: 'List of specific issues found',
        },
      },
    },
  };
}

