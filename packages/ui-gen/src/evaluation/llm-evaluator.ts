export interface EvalContext {
  originalPrompt: string;
  contract?: unknown;
  designSystemSummary?: string;
}

export function buildMotherPrompt(ctx: EvalContext): string {
  const parts: string[] = [];

  parts.push(`You are evaluating a generated React component built with the ggui design system.
The code has already passed automated checks (compilation, security, imports, design tokens).
Your job is to evaluate ONE specific quality criterion per call.`);

  parts.push(`## About This Prompt
This system prompt has 2 sections:
1. **Context** — the original user request and data contract
2. **Criteria Reference** — definitions for all 7 evaluation criteria (your call will focus on one)

## Input Messages
You will receive ONE user message per call containing:
1. Which criterion to evaluate (e.g., "Evaluate FUNCTIONALITY")
2. The source code with line numbers
3. Example outputs showing the expected tool response format

Respond with a single tool call (evaluate_{criterion}). Report ALL issues you find for that criterion, not just the first one. Each issue should be an actionable fix instruction.

## Issue-array discipline (applies to EVERY criterion)
The "issues" array is for confirmed defects ONLY. Before adding an entry, it must be something you are CERTAIN is missing or broken. NEVER add:
- a self-negating note ("…actually this is fine", "so no issue here", "no actual crash scenarios are present");
- a speculative / conditional note ("if the data is shaped X then…", "this may break when…");
- a "should be verified" / "double-check that…" note.
If you are not certain something is broken, it is a PASS — omit it entirely. An entry you describe as acceptable still counts against the score, so describing a non-issue is worse than saying nothing.`);

  parts.push(`## Original Request
${ctx.originalPrompt}`);

  if (ctx.contract) {
    parts.push(`## Data Contract (JSON Schema → TypeScript types)

The component's boilerplate was generated from this contract. All types are compiler-enforced:
- \`props\` → \`interface Props\` — every field must be rendered in the UI
- \`actionSpec\` (flat map keyed by action name) → \`useAction<Payload>(name)\` hooks — each must be wired to a UI element (button, form, etc.)
- \`actionSpec[name].nextStep\` (optional) — names an agent-side MCP tool the agent SHOULD invoke on its
  next turn after the action fires. Surfaces as advisory event metadata; the component does NOT call
  the named tool. Use it to judge whether button labels/icons match the underlying action's intent.
- \`streamSpec\` (flat map keyed by channel name) → \`useStream<T>(name)\` hooks — each must be consumed with null guard (\`.latest && ...\`)
- \`agentCapabilities.tools\` is a READ-ONLY catalog — the AGENT invokes these. The component never calls them.
  References surface only via \`actionSpec[*].nextStep\` and \`streamSpec[*].source.tool\`. Do NOT flag
  missing component-side calls for these.
- \`clientCapabilities.gadgets\` → a map keyed by npm PACKAGE name; each package maps export
  names to a descriptor. Two kinds, both contract features that MUST be used:
  • Built-in browser capabilities (\`useGeolocation\`, \`useCamera\`, \`useClipboardWrite\`,
    \`useMicrophone\`, \`useFilePicker\`, \`useClipboardPaste\`, \`useNotifications\`) — imported
    from \`@ggui-ai/gadgets\`.
  • Registered third-party gadgets — hooks or components — imported from their OWN package
    (e.g. \`import { useChartTheme } from '@ggui-samples/gadget-chart'\`). The boilerplate
    emits that import. Any export listed under \`clientCapabilities.gadgets\` IS part of the
    contract — NEVER flag it as "not part of the contract".

\`\`\`json
${JSON.stringify(ctx.contract).slice(0, 6000)}
\`\`\``);
  }

  if (ctx.designSystemSummary) {
    parts.push(`## Design System
${ctx.designSystemSummary}`);
  }

  parts.push(`## Design System Rules (important for evaluation)
- Spacing props (\`gap\`, \`padding\`, \`margin\`) take a t-shirt-scale name: \`gap="md"\`, \`padding="lg"\` (\`none|xs|sm|md|lg|xl|2xl\`). **These ARE design tokens** — each resolves to a \`--ggui-spacing-*\` variable. A raw \`var(--ggui-spacing-*)\` string is an accepted escape hatch. NEVER flag a scale name as "hardcoded". Only a numeric prop (\`padding={24}\`) or a raw CSS length (\`gap="13px"\`) bypasses the scale and should be warned.
- All colors must use CSS variables: \`color="var(--ggui-color-primary-600)"\` — hardcoded #hex or rgb() is a fail
- Component props that take enum/scale values — \`variant="primary"\`, \`size="lg"\`, \`gap="md"\`, \`padding="lg"\`, \`shadow="md"\`, \`radius="lg"\`, \`tone="muted"\`, \`surface="accent"\` — are all design system tokens and are always valid
- One \`export default function Component\` — helper components as named functions above it are fine

## Evaluation Criteria Reference

### Tier 1 — Critical (blocks delivery)

**functionality**: Does the component implement ALL features in the original request?
- Check every noun/verb in the request against the rendered output
- Missing feature = fail, even if other features work perfectly
- Partial implementation = fail (e.g., search UI exists but doesn't filter)

**crash**: Are there runtime crash scenarios?
- Uninitialized state used in render (e.g., items.map when items could be undefined)
- Missing event handlers referenced in JSX
- Destructuring undefined objects
- Array access without bounds checking
- Missing null/undefined guards before .map(), .filter(), .length

### Tier 2 — Quality (fail if severe, warn if improvement)

**interactivity**: Does it have interactive elements appropriate for its purpose?
- Forms should have submit buttons
- Lists should be navigable
- Editable fields should have save/cancel
- Interactive components should have hover/focus feedback

**accessibility**: Semantic HTML, labels, alt text, keyboard navigation.
- Form inputs must have associated labels (label prop or aria-label)
- Images need alt text
- Interactive elements need keyboard support
- Proper heading hierarchy (h1 > h2 > h3)

**layout**: Spacing, alignment, visual grouping.
- Consistent spacing between sections
- Related items grouped visually
- No overlapping or cramped elements
- Proper use of containers and cards for grouping

**loading**: Loading/error/empty states for dynamic data.
- Components receiving async data should show loading state
- Error boundaries or error messages for failure cases
- Empty states ("No items found") when data arrays could be empty

**visual**: Design system consistency.
- Uses CSS variables (var(--ggui-*)) for colors, not hardcoded values
- Consistent use of spacing tokens
- Proper use of component variants (primary, outline, ghost)
- Shadow, border-radius from design system tokens

## Primitive Accessibility (built-in — do NOT flag as missing)

ggui design-system primitives ship their own ARIA. The code you are
reading imports them from \`@ggui-ai/design\`; the source does NOT show
their internal markup. NEVER report a primitive below as missing a
role, aria-*, label, or keyboard support — it is already there:

- \`Input\` / \`Select\` / \`TextArea\` — render a real \`<label htmlFor>\` from
  the \`label\` prop, plus \`aria-invalid\` + \`aria-describedby\` + an error
  \`role="alert"\`. A primitive that has a \`label\` prop IS labelled.
- \`RadioGroup\` — \`role="radiogroup"\` + \`aria-labelledby\` from its \`label\`.
- \`Checkbox\` — wrapped in its own \`<label>\`.
- \`Toggle\` — \`role="switch"\` + \`aria-checked\` + \`aria-label\`.
- \`Progress\` — \`role="progressbar"\` + \`aria-valuenow/min/max\` + \`aria-label\`.
- \`Slider\` — full \`aria-valuenow/min/max\` slider semantics.
- \`Spinner\` — \`role="status"\` + \`aria-label="Loading"\`.
- \`Skeleton\` — \`aria-hidden\` (correctly decorative).
- \`Tabs\` — full \`role="tablist"/"tab"/"tabpanel"\` + \`aria-selected/controls\`.
- \`Accordion\` — \`aria-expanded\` + \`aria-controls\` + \`role="region"\`.
- \`Alert\` — \`role="alert"\`; \`Toast\` — \`role="alert"\` + \`aria-live\`.
- \`Tooltip\` — \`role="tooltip"\`.
- \`Clickable\` (trait, applied via \`as={Clickable}\`) — \`role="button"\` +
  \`tabIndex={0}\` + Enter/Space keyboard activation.
- \`Icon\` — decorative by default (\`aria-hidden\`); exposes \`role="img"\` +
  the label ONLY when an \`aria-label\` prop is passed. A bare \`<Icon>\` next
  to text is CORRECTLY silent — never ask for \`aria-hidden\`, it is there.

REAL accessibility gaps worth flagging:
- A raw \`<div>\` / \`<span>\` used as a click target without \`as={Clickable}\`
  (no role, no keyboard) — or any hand-rolled interactive element.
- An \`<img>\` / \`<Image>\` with no alt text.
- \`<Input>\` / \`<Select>\` / \`<TextArea>\` used with NO \`label\` prop.
- An icon-only \`<Button>\` (no text child) with no \`aria-label\`.
- Live or streaming data (stream \`.latest\`, counters, real-time clocks)
  rendered in a plain element with no \`aria-live\` / \`role="status"\`.
- Inverted or skipped heading levels (an \`<Heading level={3}>\` rendered
  before the page's \`<Heading level={1}>\`).`);

  return parts.join('\n\n');
}

