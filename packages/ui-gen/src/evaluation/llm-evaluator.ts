interface CriterionResult {
  criterion: string;
  tier: 1 | 2;
  issues: EvalIssue[];
  pass: boolean;
  tokens: { in: number; out: number };
  durationMs: number;
}

/** All valid categories for LLM evaluation */
const TIER_1_CRITERIA = ['functionality', 'crash'] as const;
const TIER_2_CRITERIA = ['interactivity', 'accessibility', 'layout', 'loading', 'visual'] as const;

async function evalCriterion(
  criterion: string,
  tier: 1 | 2,
  motherPrompt: string,
  numberedSource: string,
  agent: LLMAgent,
  model: string,
): Promise<CriterionResult> {
  const start = Date.now();
  const tool = tier === 1 ? makeTier1Tool(criterion) : makeTier2Tool(criterion);
  const userPrompt = getCriterionPrompt(criterion, numberedSource);

  try {
    const response = await agent.callTools(
      model,
      motherPrompt,
      userPrompt,
      [tool],
      'required',
    );

    const call = response.toolCalls[0];
    const durationMs = Date.now() - start;

    if (!call) {
      console.warn(`[eval] ${criterion}: no tool call returned`);
      return {
        criterion,
        tier,
        issues: [],
        pass: true,
        tokens: { in: response.inputTokens, out: response.outputTokens },
        durationMs,
      };
    }

    const raw = call.input as JsonObject;
    const issues: EvalIssue[] = [];
    let pass: boolean;

    if (tier === 1) {
      // Tier 1: binary pass/fail
      pass = raw.pass === true;
      const rawIssues = coerceStringArray(raw.issues);
      for (const desc of rawIssues) {
        issues.push({
          tier: 1,
          result: 'fail',
          category: criterion as EvalCategory,
          severity: 'critical',
          description: desc,
          fix: desc, // The issue description often includes the fix for tier 1
        });
      }
      // If pass is false but no issues listed, add a generic one
      if (!pass && issues.length === 0) {
        issues.push({
          tier: 1,
          result: 'fail',
          category: criterion as EvalCategory,
          severity: 'critical',
          description: `${criterion} check failed`,
          fix: `Fix ${criterion} issues`,
        });
      }
    } else {
      // Tier 2: fail/warn/pass
      const result = (raw.result as string) ?? 'pass';
      pass = result === 'pass';
      const rawIssues = coerceStringArray(raw.issues);
      const outcome = result === 'fail' ? 'fail' : 'warn';
      for (const desc of rawIssues) {
        issues.push({
          tier: 2,
          result: outcome as 'fail' | 'warn',
          category: criterion as EvalCategory,
          severity: result === 'fail' ? 'critical' : 'major',
          description: desc,
          fix: desc,
        });
      }
    }

    return {
      criterion,
      tier,
      issues,
      pass,
      tokens: { in: response.inputTokens, out: response.outputTokens },
      durationMs,
    };
  } catch (e) {
    const durationMs = Date.now() - start;
    console.error(`[eval] ${criterion}: error — ${e instanceof Error ? e.message : e}`);
    // On error, treat as pass to avoid blocking on flaky LLM calls
    return {
      criterion,
      tier,
      issues: [],
      pass: true,
      tokens: { in: 0, out: 0 },
      durationMs,
    };
  }
}

