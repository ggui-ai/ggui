/**
 * Safely coerce LLM tool output to string[].
 *
 * LLMs sometimes return `issues` as a JSON string instead of an array.
 * Iterating a string goes character-by-character, creating thousands of
 * single-char "issues". This helper handles: string[] (pass-through),
 * string (JSON parse), or anything else (empty array).
 */
function coerceStringArray(value: unknown): string[] {
  if (Array.isArray(value)) {
    return value.filter((v): v is string => typeof v === 'string' && v.length > 1);
  }
  if (typeof value === 'string') {
    try {
      const parsed = JSON.parse(value);
      if (Array.isArray(parsed)) {
        return parsed.filter((v): v is string => typeof v === 'string' && v.length > 1);
      }
    } catch {
      // Not JSON — treat the whole string as a single issue
      if (value.length > 1) return [value];
    }
  }
  return [];
}

