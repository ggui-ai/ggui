function getCriterionPrompt(criterion: string, numberedSource: string): string {
  const sourceBlock = `\nSource code (with line numbers):\n\`\`\`tsx\n${numberedSource}\n\`\`\``;

  // Use criteria.ts as single source of truth when available
  const criterionDef = getCriterionById(criterion);
  if (criterionDef && criterionDef.tier > 0) {
    return `${criterionDef.evalInstruction}
${sourceBlock}

Return your evaluation using the evaluate_${criterion} tool.`;
  }

  // Fallback for criteria not yet in criteria.ts

  switch (criterion) {
    case 'functionality':
      return `Evaluate FUNCTIONALITY: Does this component implement ALL features from the request AND the data contract?

Check against BOTH sources:
1. **Original request** — each feature must be coded AND rendered in JSX
2. **Data contract** (if present in the context above) — verify:
   - ALL Props fields are rendered somewhere in the UI (not just accepted as params)
   - ALL useAction hooks are wired to clickable UI elements (buttons, forms, cards)
   - ALL useStream hooks have their \`.latest\` data rendered (with null guard)
   - ALL clientCapabilities hooks (useGeolocation / useCamera / etc.) are called in the component body and their values either render directly or thread into a context / action payload

A contract hook that is declared but never used in JSX is a MISSING feature.

**CRITICAL RULE:** The "issues" array must ONLY contain MISSING or BROKEN features. Do NOT list features that ARE implemented.

Design token usage (hardcoded colors, numeric spacing) is NOT a functionality issue — ignore it here.
${sourceBlock}

Return your evaluation using the evaluate_functionality tool.

Example outputs:
- All features present: {"pass": true}
- Missing features: {"pass": false, "issues": ["Add Task form is not rendered — only state logic exists", "useAction('refresh') is declared but never wired to any button"]}

Only missing or broken features go in the issues array. Nothing else.`;

    case 'crash':
      return `Evaluate CRASH SAFETY: Are there ACTUAL runtime crash scenarios in this component?

**CRITICAL RULE:** The "issues" array must ONLY contain code that WILL crash. Do NOT include safe code in the issues array — not even to explain why it's safe. If you checked a line and it has a guard, do not mention it at all.

**These WILL crash (include in issues):**
- .map() / .filter() / .length on a variable that is never initialized
- Accessing a property of a value that could be undefined without a guard
- Calling a function that is never defined
- useStream().latest.field WITHOUT null guard — .latest is T | null
- Optional Props field (has \`?\` in interface) accessed as props.field.x without guard — WILL crash if undefined
- Array item field that could be undefined: items.map(item => item.priority.toUpperCase()) when priority is optional

**These are SAFE (do NOT include in issues):**
- Optional chaining: props.items?.map() — SAFE, skip it
- Fallback values: items || [] — SAFE, skip it
- Default parameters: (items = []) — SAFE, skip it
- useState initializer: useState([]) — SAFE, skip it
- Null check: items && items.map() — SAFE, skip it
- stream.latest && stream.latest.field — SAFE, guarded
- stream.all.map(...) — SAFE, .all is always an array (never null)
- stream.all.length — SAFE, always a number
- toolResult.data && toolResult.data.field — SAFE, guarded

If everything is safe, return {"pass": true} with no issues array.
${sourceBlock}

Return your evaluation using the evaluate_crash tool.

Example outputs:
- Everything safe: {"pass": true}
- Has crash: {"pass": false, "issues": ["Line 41: selectedItem.name — selectedItem comes from .find() which returns undefined when no match, no guard"]}

Only unguarded crash risks go in the issues array. Nothing else.`;

    case 'interactivity':
      return `Evaluate INTERACTIVITY: Does this component have sufficient interactive elements for its purpose?

Consider: forms need submit buttons, lists need selection/navigation, editable content needs save/cancel, interactive elements need hover/focus feedback.

**Contract actions** (if present): every useAction hook from the data contract should be triggered by a visible, clickable UI element. A declared action with no triggering button/link/form is a missing interaction.

**Only list MISSING interactive elements in the issues array.** Do not list interactions that are present and working.
${sourceBlock}

Return your evaluation using the evaluate_interactivity tool.

Example outputs:
- Sufficient: {"result": "pass"}
- Minor gap: {"result": "warn", "issues": ["No hover feedback on list items — users can't tell items are clickable"]}
- Severe gap: {"result": "fail", "issues": ["Form has no submit button — users cannot complete the workflow"]}

Use 'fail' only for issues that block the component's core purpose.`;

    case 'accessibility':
      return `Evaluate ACCESSIBILITY: Check for missing labels, alt text, semantic HTML, keyboard navigation.

Look for: form inputs without labels, images without alt text, non-semantic elements used for interactive purposes, missing aria attributes, heading hierarchy issues.

**Only list MISSING accessibility features in the issues array.** Do not list things that are already accessible.
${sourceBlock}

Return your evaluation using the evaluate_accessibility tool.

Example outputs:
- Clean: {"result": "pass"}
- Minor issues: {"result": "warn", "issues": ["Email input missing label prop", "Task edit uses double-click with no keyboard alternative"]}
- Severe: {"result": "fail", "issues": ["No form inputs have labels — entire form is inaccessible to screen readers"]}

Use 'fail' only if the issue blocks delivery. Use 'warn' for improvements.`;

    case 'layout':
      return `Evaluate LAYOUT: Check spacing, alignment, visual grouping, and overall composition.

Look for: inconsistent spacing, cramped elements, missing visual grouping (cards/sections), overlapping concerns in single containers, poor responsive patterns.

**Only list ACTUAL layout problems in the issues array.** Do not list layout aspects that are working correctly.
${sourceBlock}

Return your evaluation using the evaluate_layout tool.

Example outputs:
- Clean: {"result": "pass"}
- Minor issues: {"result": "warn", "issues": ["Header and content area have no visual separation", "Button group lacks consistent spacing"]}
- Severe: {"result": "fail", "issues": ["All content rendered in a flat list with no visual structure — needs cards or sections"]}

Use 'fail' only for layouts that are fundamentally broken.`;

    case 'loading':
      return `Evaluate LOADING/EMPTY/ERROR STATES: Does the component handle async data, empty collections, and error cases?

Look for: arrays that could be empty but have no empty state message, missing error handling for operations that can fail.

**Contract-specific checks** (if present):
- useStream hooks deliver data over time — consider showing a "waiting for data" state before .latest arrives
- clientCapabilities hooks (e.g. useGeolocation) may return permission denials or undefined values — defensive null guards expected before threading into JSX

**Only list MISSING states in the issues array.** Do not list states that are already handled. Components that receive all data via props (no streams, no async capabilities) do NOT need loading states — return pass.
${sourceBlock}

Return your evaluation using the evaluate_loading tool.

Example outputs:
- Not applicable (props-driven): {"result": "pass"}
- Missing states: {"result": "warn", "issues": ["No empty state when items array is empty — should show 'No items found'"]}
- Severe: {"result": "fail", "issues": ["Component shows blank screen when data hasn't loaded yet — needs loading spinner"]}`;

    case 'visual':
      return `Evaluate VISUAL CONSISTENCY: Is the component using the design system correctly?

Look for: hardcoded colors instead of CSS variables, numeric/raw-CSS spacing props that bypass the t-shirt scale, style objects that bypass the design system.

**Only list ACTUAL design system violations in the issues array.** Do not list correct token usage. A t-shirt-scale spacing name (\`gap="md"\`, \`padding="lg"\`) IS correct token usage — never flag it as hardcoded; only numeric (\`padding={16}\`) or raw-CSS-length (\`gap="13px"\`) spacing is a violation. Note: intentional custom colors (e.g., status indicators, category colors) are acceptable when no semantic token fits.
${sourceBlock}

Return your evaluation using the evaluate_visual tool.

Example outputs:
- Clean: {"result": "pass"}
- Minor issues: {"result": "warn", "issues": ["Line 45: hardcoded '#f3f4f6' should use var(--ggui-color-surfaceVariant)", "Line 80: numeric padding={16} should use a scale name like padding='md'"]}
- Severe: {"result": "fail", "issues": ["Component uses entirely custom styling with no design system tokens"]}

Use 'fail' only for pervasive design system violations. Use 'warn' for isolated cases.`;

    default:
      return `Evaluate ${criterion.toUpperCase()}.${sourceBlock}\n\nReturn your evaluation using the evaluate_${criterion} tool.`;
  }
}

