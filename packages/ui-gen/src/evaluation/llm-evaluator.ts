/**
 * Pre-warm evaluation context during the coding phase.
 *
 * Builds the mother prompt during coding so it's ready when eval starts.
 * The result can be passed to runLLMEvaluation() to avoid redundant work.
 */
export async function preWarmEval(
  context: LLMEvalContext,
  _config: LLMEvalConfig,
): Promise<PreWarmedEvalContext> {
  const motherPrompt = buildMotherPrompt({
    originalPrompt: context.originalPrompt,
    contract: context.contract,
    designSystemSummary: context.designContext,
  });

  return { motherPrompt, dynamicCriteria: [] };
}

