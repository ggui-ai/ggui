/** Tier 1 categories — always tier 1 regardless of what LLM returns */
const TIER_1_CATEGORIES = new Set<string>(['functionality', 'crash']);

/** Tier 2 categories */
const TIER_2_CATEGORIES = new Set<string>([
  'interactivity',
  'accessibility',
  'layout',
  'loading',
  'visual',
]);

/** All valid categories for LLM evaluation */
const VALID_CATEGORIES = new Set<string>([
  ...TIER_1_CATEGORIES,
  ...TIER_2_CATEGORIES,
]);

/**
 * Parse the raw tool call input into a typed EvalResult.
 *
 * Exported for testing — not part of the public API.
 * Retained for backward compatibility with existing tests.
 */
export function parseEvalResponse(raw: JsonObject): EvalResult {
  const rawIssues = (raw.issues ?? []) as Array<JsonObject>;
  const rawPass = (raw.pass ?? []) as string[];

  const issues: EvalIssue[] = rawIssues
    .filter((i) => {
      const cat = i.category as string;
      return VALID_CATEGORIES.has(cat);
    })
    .map((i) => {
      const category = i.category as string;
      const isTier1 = TIER_1_CATEGORIES.has(category);

      // Enforce correct tier based on category
      const tier = isTier1 ? 1 : 2;

      // Tier 1 issues are always 'fail' (critical checks)
      // Tier 2 issues: LLM decides 'fail' or 'warn'
      const result = isTier1
        ? 'fail'
        : ((i.result as string) === 'fail' ? 'fail' : 'warn');

      return {
        tier: tier as 1 | 2,
        result: result as 'fail' | 'warn',
        category: category as EvalCategory,
        severity: (i.severity as 'critical' | 'major') || (isTier1 ? 'critical' : 'major'),
        description: (i.description as string) || '',
        fix: (i.fix as string) || '',
      };
    });

  return {
    issues,
    pass: rawPass,
  };
}

