// packages/ui-gen/src/evaluation/llm-evaluator.ts
//
// Parallel per-criterion LLM evaluator with cached mother prompt.
//
// Architecture:
//   1. Mother prompt (cached, sent once): user request + contract + design system + all criteria definitions
//   2. 7 parallel per-criterion LLM calls, each reusing the cached mother prompt
//   3. Each call returns a focused tool response for one criterion
//
// Tiers:
//   Tier 1 — Critical (always blocks): functionality, crash → { pass: boolean, issues?: string[] }
//   Tier 2 — Quality (LLM decides):    interactivity, accessibility, layout, loading, visual → { result: 'fail'|'warn'|'pass', issues?: string[] }
//
// Caching: Anthropic ephemeral cache, OpenAI auto-cache, Google explicit cache.
// Same system prompt across all 7 calls → first call caches, 6 remaining hit cache.

