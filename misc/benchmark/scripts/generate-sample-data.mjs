#!/usr/bin/env node
/**
 * Demo sample-data generator.
 *
 * Drives the REAL reporter serialization path (`generateReport` →
 * `toDisplayReport`) with synthetic run results — no LLM calls — and
 * writes the viewer's on-disk convention:
 *
 *   <out>/index.json
 *   <out>/<date>/multi-sdk.json
 *
 * Exists so demo/sample data can never drift from the production
 * report shape again (the previous hand-written sample data carried
 * runner-internal field names like `finalScore` and masked a viewer
 * bug that made every real report render wrong).
 *
 * Usage (monorepo: regenerate the benchmarks app demo data):
 *   node --import tsx scripts/generate-sample-data.mjs \
 *     --out ../../../apps/benchmarks/public/sample-data
 *
 * The data is deterministic — same input, same bytes (timestamps are
 * fixed) — so regeneration produces reviewable diffs.
 */

import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { buildHeadline } from './headline.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const BENCH_ROOT = resolve(__dirname, '..');

const args = process.argv.slice(2);
let outDir = null;
for (let i = 0; i < args.length; i++) {
  if (args[i] === '--out') outDir = args[i + 1] ?? null;
}
if (!outDir) {
  console.error('[sample-data] --out <dir> is required');
  process.exit(2);
}
const OUT = resolve(process.cwd(), outDir);

const { generateReport, toDisplayReport } = await import(
  resolve(BENCH_ROOT, 'src/multi-sdk/reporter.ts')
);
const { AESTHETIC_JUDGE_MODEL, AESTHETIC_PROMPT_VERSION } = await import(
  resolve(BENCH_ROOT, 'src/multi-sdk/post-eval.ts')
);

// ── Synthetic corpus ────────────────────────────────────────────────

const VARIANTS = [
  { id: 'claude-0', sdkName: 'claude', tier: 'balanced', modelId: 'anthropic/claude-haiku-4-5' },
  { id: 'openai-1', sdkName: 'openai', tier: 'balanced', modelId: 'openai/gpt-5.4-mini' },
  { id: 'google-2', sdkName: 'google', tier: 'balanced', modelId: 'gemini/gemini-3.1-flash-lite' },
];

const COMMITS = [
  { id: 'weather-card', name: 'Weather Card', complexity: 'simple' },
  { id: 'survey-form', name: 'Survey Form', complexity: 'medium' },
  { id: 'kanban-board', name: 'Kanban Board', complexity: 'medium' },
  { id: 'stock-ticker', name: 'Stock Ticker', complexity: 'simple' },
];

/** Deterministic pseudo-score per (variant, commit, day) — no RNG. */
function syntheticScore(vIdx, cIdx, dayIdx) {
  const base = [86, 79, 73][vIdx];
  return base + ((cIdx * 3 + dayIdx * 2) % 7) - 3;
}

function mkEval(score, vIdx, cIdx) {
  return {
    passed: score >= 70,
    score,
    dimensions: {
      layout: Math.min(100, score + 3),
      designTokens: Math.max(0, score - 4),
      hierarchy: score,
      polish: Math.max(0, score - 2),
      dataPresentation: Math.min(100, score + 3),
    },
    judge: { model: AESTHETIC_JUDGE_MODEL, promptVersion: AESTHETIC_PROMPT_VERSION },
    issues: [],
    critique: 'Synthetic demo result — generated by scripts/generate-sample-data.mjs.',
    evalTimeMs: 1100 + vIdx * 130 + cIdx * 60,
  };
}

function mkResults(date, dayIdx) {
  const results = [];
  VARIANTS.forEach((variant, vIdx) => {
    COMMITS.forEach((commit, cIdx) => {
      const timestamp = `${date}T03:0${vIdx}:${String(10 + cIdx * 7).padStart(2, '0')}.000Z`;
      // One failure cell per day (google × kanban-board) and one
      // unevaluated cell (openai × stock-ticker) so the demo exercises
      // the dashboard's "fail" and "—" paths, not just happy rows.
      if (variant.sdkName === 'google' && commit.id === 'kanban-board') {
        results.push({
          variant,
          commit: { ...commit, description: '', prompt: '', contract: {} },
          generation: null,
          evaluation: null,
          estimatedCostUsd: 0,
          error: 'Timeout after 300000ms',
          timestamp,
          generator: 'ui-gen-default-haiku-4-5',
        });
        return;
      }
      const evaluation =
        variant.sdkName === 'openai' && commit.id === 'stock-ticker'
          ? null
          : mkEval(syntheticScore(vIdx, cIdx, dayIdx), vIdx, cIdx);
      results.push({
        variant,
        commit: { ...commit, description: '', prompt: '', contract: {} },
        generation: {
          compiledCode: `// demo ${variant.sdkName} × ${commit.id}`,
          sourceCode: `// demo ${variant.sdkName} × ${commit.id}`,
          tokens: {
            input: 12000 + vIdx * 1500 + cIdx * 800,
            output: 1800 + vIdx * 300 + cIdx * 150,
            total: 13800 + vIdx * 1800 + cIdx * 950,
          },
          generationTimeMs: 21000 + vIdx * 6000 + cIdx * 3500 + dayIdx * 900,
          turnsUsed: 2 + ((vIdx + cIdx) % 3),
        },
        evaluation,
        estimatedCostUsd: 0.012 + vIdx * 0.006 + cIdx * 0.003,
        timestamp,
        generator: 'ui-gen-default-haiku-4-5',
      });
    });
  });
  return results;
}

// ── Emit ────────────────────────────────────────────────────────────

const DATES = ['2026-05-05', '2026-05-06'];
const indexRuns = [];

DATES.forEach((date, dayIdx) => {
  const results = mkResults(date, dayIdx);
  const report = generateReport(results, 415000 + dayIdx * 9000);
  // Pin the timestamp for deterministic output (generateReport stamps
  // "now"; demo data should not churn on regeneration).
  report.meta.timestamp = `${date}T03:10:00.000Z`;
  const display = toDisplayReport(report, `sample-${date}`, 'sample');

  const dir = resolve(OUT, date);
  mkdirSync(dir, { recursive: true });
  writeFileSync(resolve(dir, 'multi-sdk.json'), JSON.stringify(display, null, 2) + '\n');

  indexRuns.unshift({
    date,
    multiSdk: {
      reportPath: `${date}/multi-sdk.json`,
      successRate: display.meta.successRate,
      totalRuns: display.meta.totalRuns,
      headline: buildHeadline(display),
    },
  });
});

writeFileSync(
  resolve(OUT, 'index.json'),
  JSON.stringify(
    {
      schemaVersion: 'benchmark-index.v0',
      generatedAt: `${DATES[DATES.length - 1]}T03:10:00.000Z`,
      runs: indexRuns,
    },
    null,
    2,
  ) + '\n',
);

console.log(`[sample-data] wrote ${DATES.length} reports + index.json → ${OUT}`);
