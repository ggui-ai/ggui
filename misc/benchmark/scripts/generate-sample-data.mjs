#!/usr/bin/env node
/**
 * Demo sample-data generator.
 *
 * Drives the REAL reporter serialization path (`generateReport` →
 * `toDisplayReport`) with synthetic run results — no LLM calls — and
 * writes the viewer's on-disk convention:
 *
 *   <out>/index.json
 *   <out>/<date>/multi-sdk.json
 *
 * Exists so demo/sample data can never drift from the production
 * report shape again (the previous hand-written sample data carried
 * runner-internal field names like `finalScore` and masked a viewer
 * bug that made every real report render wrong).
 *
 * Usage (monorepo: regenerate the benchmarks app demo data):
 *   node --import tsx scripts/generate-sample-data.mjs \
 *     --out ../../../apps/benchmarks/public/sample-data
 *
 * The data is deterministic — same input, same bytes (timestamps are
 * fixed) — so regeneration produces reviewable diffs.
 */

import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { buildHeadline } from './headline.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const BENCH_ROOT = resolve(__dirname, '..');

const args = process.argv.slice(2);
let outDir = null;
for (let i = 0; i < args.length; i++) {
  if (args[i] === '--out') outDir = args[i + 1] ?? null;
}
if (!outDir) {
  console.error('[sample-data] --out <dir> is required');
  process.exit(2);
}
const OUT = resolve(process.cwd(), outDir);

const { generateReport, toDisplayReport } = await import(
  resolve(BENCH_ROOT, 'src/multi-sdk/reporter.ts')
);
const { AESTHETIC_PROMPT_VERSION_PANEL } = await import(
  resolve(BENCH_ROOT, 'src/multi-sdk/post-eval.ts')
);
const { getDefaultVariants } = await import(
  resolve(BENCH_ROOT, 'src/multi-sdk/variants.ts')
);

// ── Synthetic corpus ────────────────────────────────────────────────

// The full 9-cell nightly matrix (3 SDKs × 3 tiers), pulled from the
// real variant catalogue so the demo never drifts from the variant ids,
// tiers, and model ids the nightly actually runs.
const VARIANTS = getDefaultVariants();

// The judge panel surfaced on every synthetic cell — one model per
// provider, mirroring the real `PANEL` in post-eval.ts. The panel
// aggregator (and the viewer) reads `judges[]`, derives `spread`, and
// renders the per-judge breakdown, so the demo must carry all three.
const PANEL_MODELS = [
  'claude-haiku-4-5-20251001',
  'gpt-5.4-mini',
  'gemini-3-flash-preview',
];

const COMMITS = [
  { id: 'weather-card', name: 'Weather Card', complexity: 'simple' },
  { id: 'survey-form', name: 'Survey Form', complexity: 'medium' },
  { id: 'kanban-board', name: 'Kanban Board', complexity: 'medium' },
  { id: 'stock-ticker', name: 'Stock Ticker', complexity: 'simple' },
];

/**
 * Deterministic pseudo-score per (variant, commit, day) — no RNG. The
 * base climbs with the model tier (fast < balanced < premium) so the
 * 9-cell matrix reads plausibly; `vIdx` spans all nine variants.
 */
function syntheticScore(vIdx, cIdx, dayIdx) {
  const tier = VARIANTS[vIdx]?.tier;
  const base = tier === 'premium' ? 88 : tier === 'balanced' ? 82 : 76;
  return base + ((vIdx * 2 + cIdx * 3 + dayIdx * 2) % 7) - 3;
}

/** Clamp a 0-100 score. */
function clamp(n) {
  return Math.max(0, Math.min(100, n));
}

/** Equal-weighted 5-dimension mean, 1dp — mirrors post-eval `weightedScore`. */
function weightedScore(dims) {
  const raw =
    (dims.layout + dims.designTokens + dims.hierarchy + dims.polish + dims.dataPresentation) / 5;
  return Math.round(raw * 10) / 10;
}

/**
 * Build a single judge's contribution. Each provider judge nudges the
 * cell's base score by a deterministic offset so the panel has a
 * realistic ±3-5 spread (disagreement signal) rather than three
 * identical scores.
 */
function mkJudge(model, baseScore, offset, vIdx, cIdx, jIdx) {
  const s = clamp(baseScore + offset);
  const dimensions = {
    layout: clamp(s + 3),
    designTokens: clamp(s - 4),
    hierarchy: clamp(s),
    polish: clamp(s - 2),
    dataPresentation: clamp(s + 3),
  };
  return {
    judge: { model, promptVersion: AESTHETIC_PROMPT_VERSION_PANEL },
    score: weightedScore(dimensions),
    dimensions,
    critique:
      `Synthetic demo critique from ${model} — generated by ` +
      'scripts/generate-sample-data.mjs.',
    tokens: { input: 1400 + jIdx * 120 + cIdx * 40, output: 180 + jIdx * 20 + vIdx * 10 },
  };
}

/**
 * Build a {@link PanelEvalResult}-shaped synthetic evaluation. The three
 * provider judges score around `score` with small offsets; the panel
 * aggregate (`score`, `dimensions`) is the mean and `spread` is
 * max−min of the per-judge weighted scores. The reporter maps this into
 * the viewer's `judges[]` / `panel[]` / `spread` display shape.
 */
function mkEval(score, vIdx, cIdx) {
  // Deterministic per-judge offsets that vary by cell so the panel
  // shows a realistic ±3-5 spread (disagreement signal) rather than
  // three identical scores. Kept inside ±5 of the base.
  const offsets = [
    (cIdx % 3) - 1,
    ((vIdx + 2) % 5) - 2,
    -((cIdx + vIdx) % 5) + 2,
  ];
  const judges = PANEL_MODELS.map((model, jIdx) =>
    mkJudge(model, score, offsets[jIdx], vIdx, cIdx, jIdx),
  );

  const judgeScores = judges.map((j) => j.score);
  const mean = Math.round((judgeScores.reduce((a, b) => a + b, 0) / judges.length) * 10) / 10;
  const spread = Math.round((Math.max(...judgeScores) - Math.min(...judgeScores)) * 10) / 10;

  const dimKeys = ['layout', 'designTokens', 'hierarchy', 'polish', 'dataPresentation'];
  const dimensions = {};
  for (const k of dimKeys) {
    const avg = judges.reduce((a, j) => a + j.dimensions[k], 0) / judges.length;
    dimensions[k] = Math.round(avg * 10) / 10;
  }

  return {
    passed: mean >= 70,
    score: mean,
    dimensions,
    spread,
    judges,
    promptVersion: AESTHETIC_PROMPT_VERSION_PANEL,
    critique: 'Synthetic demo result — generated by scripts/generate-sample-data.mjs.',
    evalTimeMs: 1100 + vIdx * 130 + cIdx * 60,
  };
}

function mkResults(date, dayIdx) {
  const results = [];
  VARIANTS.forEach((variant, vIdx) => {
    COMMITS.forEach((commit, cIdx) => {
      const timestamp = `${date}T03:0${vIdx}:${String(10 + cIdx * 7).padStart(2, '0')}.000Z`;
      // One failure cell per day (google × kanban-board) and one
      // unevaluated cell (openai × stock-ticker) so the demo exercises
      // the dashboard's "fail" and "—" paths, not just happy rows.
      if (variant.sdkName === 'google' && commit.id === 'kanban-board') {
        results.push({
          variant,
          commit: { ...commit, description: '', prompt: '', contract: {} },
          generation: null,
          evaluation: null,
          estimatedCostUsd: 0,
          error: 'Timeout after 300000ms',
          timestamp,
          generator: 'ui-gen-default-haiku-4-5',
        });
        return;
      }
      const evaluation =
        variant.sdkName === 'openai' && commit.id === 'stock-ticker'
          ? null
          : mkEval(syntheticScore(vIdx, cIdx, dayIdx), vIdx, cIdx);
      results.push({
        variant,
        commit: { ...commit, description: '', prompt: '', contract: {} },
        generation: {
          compiledCode: `// demo ${variant.sdkName} × ${commit.id}`,
          sourceCode: `// demo ${variant.sdkName} × ${commit.id}`,
          tokens: {
            input: 12000 + vIdx * 1500 + cIdx * 800,
            output: 1800 + vIdx * 300 + cIdx * 150,
            total: 13800 + vIdx * 1800 + cIdx * 950,
          },
          generationTimeMs: 21000 + vIdx * 6000 + cIdx * 3500 + dayIdx * 900,
          turnsUsed: 2 + ((vIdx + cIdx) % 3),
        },
        evaluation,
        estimatedCostUsd: 0.012 + vIdx * 0.006 + cIdx * 0.003,
        timestamp,
        generator: 'ui-gen-default-haiku-4-5',
      });
    });
  });
  return results;
}

// ── Emit ────────────────────────────────────────────────────────────

const DATES = ['2026-05-05', '2026-05-06'];
const indexRuns = [];

DATES.forEach((date, dayIdx) => {
  const results = mkResults(date, dayIdx);
  const report = generateReport(results, 415000 + dayIdx * 9000);
  // Pin the timestamp for deterministic output (generateReport stamps
  // "now"; demo data should not churn on regeneration).
  report.meta.timestamp = `${date}T03:10:00.000Z`;
  const display = toDisplayReport(report, `sample-${date}`, 'sample');

  const dir = resolve(OUT, date);
  mkdirSync(dir, { recursive: true });
  writeFileSync(resolve(dir, 'multi-sdk.json'), JSON.stringify(display, null, 2) + '\n');

  indexRuns.unshift({
    date,
    multiSdk: {
      reportPath: `${date}/multi-sdk.json`,
      successRate: display.meta.successRate,
      totalRuns: display.meta.totalRuns,
      headline: buildHeadline(display),
    },
  });
});

writeFileSync(
  resolve(OUT, 'index.json'),
  JSON.stringify(
    {
      schemaVersion: 'benchmark-index.v0',
      generatedAt: `${DATES[DATES.length - 1]}T03:10:00.000Z`,
      runs: indexRuns,
    },
    null,
    2,
  ) + '\n',
);

console.log(`[sample-data] wrote ${DATES.length} reports + index.json → ${OUT}`);
